#![allow(clippy::neg_multiply)]
#![allow(dead_code)]
use itertools::Itertools;
use std::cmp::Reverse;
use std::collections::{BTreeMap, BTreeSet, BinaryHeap, VecDeque};
use superslice::Ext;

use proconio::{
    fastout, input,
    marker::{Chars, Usize1},
};

#[derive(Default)]
struct Solver {}
impl Solver {
    #[fastout]
    fn solve(&mut self) {
        input! {
            S: Chars
        }
        let mut cnt = vec![0; 26];
        for s in S {
            let n = s as u8 - b'a';
            cnt[n as usize] += 1;
        }
        let mut ans = '_';
        let mut mx = 0;
        for i in 0..26 {
            if cnt[i] > mx {
                mx = cnt[i];
                ans = (i as u8 + b'a') as char;
            }
        }
        println!("{}", ans);
    }
}

fn main() {
    std::thread::Builder::new()
        .stack_size(128 * 1024 * 1024)
        .spawn(|| Solver::default().solve())
        .unwrap()
        .join()
        .unwrap();
}
